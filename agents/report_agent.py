import pandas as pd
from typing import Dict, Any, List

class ReportAgent:
    """
    Agent responsible for compiling results from all other agents and generating
    a clean, comprehensive, professional Markdown report.
    """
    def __init__(self):
        pass

    def generate_report(
        self,
        loader_summary: Dict[str, Any],
        quality_results: Dict[str, Any],
        outlier_results: Dict[str, Any],
        preprocessing_recs: List[Dict[str, Any]],
        model_recs: Dict[str, Any]
    ) -> str:
        """
        Generates a complete markdown report string from compiled agent findings.
        """
        report = []
        
        # Title & Header
        report.append("# Dataset Explorer Agent - Automated Report")
        report.append("Generated by the **Dataset Explorer Agent** (Offline Analysis Pipeline).\n")
        
        # 1. Dataset Overview
        report.append("## 1. Dataset Overview")
        report.append(f"- **Total Rows**: {loader_summary['rows']:,}")
        report.append(f"- **Total Columns**: {loader_summary['num_columns']:,}")
        report.append("- **Columns List**:")
        for col in loader_summary['columns']:
            report.append(f"  - `{col}`")
        report.append("\n### First 5 Rows Preview:")
        preview_df = loader_summary['preview']
        report.append(preview_df.to_markdown(index=False))
        report.append("\n")

        # 2. Data Quality Analysis
        report.append("## 2. Data Quality Analysis")
        report.append(f"- **Overall Quality Score**: {quality_results['quality_score']}/100 ({quality_results['quality_grade']})")
        report.append(f"- **Total Duplicate Rows**: {quality_results['duplicate_count']:,} ({quality_results['duplicate_percentage']}%)")
        report.append(f"- **Total Missing Cells**: {quality_results['total_missing']:,} ({quality_results['total_missing_percentage']}%)")
        
        report.append("\n### Missing Values & Data Types by Column:")
        missing_headers = ["Column", "Data Type", "Missing Count", "Missing Percentage (%)"]
        missing_rows = []
        for info in quality_results['missing_info']:
            missing_rows.append([
                f"`{info['column']}`",
                info['dtype'],
                f"{info['missing_count']:,}",
                f"{info['missing_percentage']:.2f}%"
            ])
        missing_df = pd.DataFrame(missing_rows, columns=missing_headers)
        report.append(missing_df.to_markdown(index=False))
        report.append("\n")

        if len(quality_results['incorrect_dtypes']) > 0:
            report.append("### Detected Data Type Inconsistencies:")
            for item in quality_results['incorrect_dtypes']:
                report.append(f"- **Column `{item['column']}`**: Currently `{item['current_type']}`, suggested `{item['suggested_type']}`.")
                report.append(f"  - *Reason*: {item['reason']}")
            report.append("\n")
        else:
            report.append("### Data Type Check:\nNo incorrect data types were detected in categorical/object columns.\n")

        # 3. Outlier Analysis Summary
        report.append("## 3. Outlier Analysis Summary")
        report.append(f"- **Total IQR Outliers Detected**: {outlier_results['total_iqr_outliers']:,}")
        report.append(f"- **Total Z-Score (|Z| > 3) Outliers Detected**: {outlier_results['total_zscore_outliers']:,}")
        report.append(f"- **Number of Numeric Columns Analyzed**: {outlier_results['num_numeric_columns']}")
        
        if outlier_results['columns']:
            report.append("\n### Outliers per Numeric Column:")
            outlier_headers = ["Column", "Mean", "Std Dev", "IQR Outlier Count", "IQR Boundaries", "Z-Score Outlier Count"]
            outlier_rows = []
            for col, summary in outlier_results['columns'].items():
                outlier_rows.append([
                    f"`{col}`",
                    f"{summary['mean']:.3f}",
                    f"{summary['std']:.3f}",
                    f"{summary['iqr_count']:,}",
                    f"[{summary['iqr_bounds'][0]}, {summary['iqr_bounds'][1]}]",
                    f"{summary['zscore_count']:,}"
                ])
            outlier_df = pd.DataFrame(outlier_rows, columns=outlier_headers)
            report.append(outlier_df.to_markdown(index=False))
            report.append("\n")
        else:
            report.append("\nNo numeric columns available for outlier analysis.\n")

        # 4. Preprocessing Recommendations
        report.append("## 4. Preprocessing Recommendations")
        for idx, rec in enumerate(preprocessing_recs, 1):
            status = "✅ Recommended" if rec['recommended'] else "ℹ️ Not Needed"
            report.append(f"### 4.{idx} {rec['action']} ({status})")
            report.append(f"- **Scope**: {rec['scope']}")
            report.append(f"- **Justification**: {rec['reason']}\n")

        # 5. Machine Learning Model Recommendations
        report.append("## 5. Machine Learning Model Recommendations")
        target_name = f"`{model_recs.get('target_column')}`" if model_recs.get('target_column') else "None Selected"
        report.append(f"- **Target Column**: {target_name}")
        report.append(f"- **ML Problem Type Detected**: **{model_recs.get('problem_detected', 'N/A')}**\n")
        report.append(f"{model_recs['explanation']}\n")
        
        if model_recs.get('recommended') and len(model_recs['models']) > 0:
            for m in model_recs['models']:
                report.append(f"### Model: {m['name']}")
                report.append(f"- **Pros**: {m['pros']}")
                report.append(f"- **Cons**: {m['cons']}")
                report.append(f"- **Why Recommended**: {m['reason']}\n")

        # 6. Conclusion
        report.append("## 6. Conclusion and Next Steps")
        report.append("Based on the data profiling, here is the suggested pipeline pathway:")
        report.append("1. **Data Cleaning**: Execute rows deduplication and impute the missing values in numeric/categorical variables using the recommended methods.")
        report.append("2. **Feature Engineering**: Standardize features using standard scaling if you plan to train linear models or support vector machines, and encode categorical variables using label or one-hot encoding.")
        report.append("3. **Cross-Validation Setup**: Partition the data using standard cross-validation strategies (Stratified K-Fold for Classification; K-Fold for Regression) before fitting models.")
        report.append("4. **Baseline Modeling**: Train the recommended models and compare their metrics (accuracy, F1-score, ROC-AUC for classification; RMSE, MAE, R² for regression).")
        
        return "\n".join(report)
